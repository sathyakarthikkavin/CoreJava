public class SwitchCase {
    public static void main(String[] args) {
        int n = 1;
        switch (n) {
            case 1:
                System.out.println("Mon");
                break;
            case 2:
                System.out.println("Tues");
            case 3:
                System.out.println("wed");
            case 4:
                System.out.println("thurs");
                Default:
                System.out.println("sunday");
                break;

        }

    }
}
